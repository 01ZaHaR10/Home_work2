a = list(map(float, input("Введите 2 числа через пробел: ").split()))
print(f"Сумма чисел: {a[0] + a[1]}")
print(f"Разность чисел: {a[0] - a[1]} или {a[1] - a[0]}")
print(f"Произведение чисел: {a[0] * a[1]}")
print(f"Частное чисел: {a[0] / a[1]} или {a[1] / a[0]}")
print(f"Возведение в степень: {a[0] ** a[1]} или {a[1] ** a[0]}")
print(f"Деление по модулю: {a[0] % a[1]} или {a[1] % a[0]}")
print(f"Целочисленное деление: {a[0] // a[1]} или {a[1] // a[0]}")